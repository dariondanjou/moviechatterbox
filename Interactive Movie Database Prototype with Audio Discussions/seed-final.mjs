async function main() {
  const conn = await mysql.createConnection(process.env.DATABASE_URL);
  const [genreRows] = await conn.execute("SELECT id, name FROM genres");
  const genreMap = {};
  for (const g of genreRows) genreMap[g.name] = g.id;

  let inserted = 0, skipped = 0;
  for (const [title, year, imdb, rt, genre] of MOVIES) {
    try {
      const [ex] = await conn.execute("SELECT id FROM movies WHERE title = ? AND year = ?", [title, year]);
      if (ex.length > 0) { skipped++; continue; }
      const slug = slugify(title) + "-" + year;
      const letter = title.charAt(0).toUpperCase();
      const poster = `https://placehold.co/300x450/1a1a1a/ff6b35?text=${encodeURIComponent(letter)}`;
      const [r] = await conn.execute(
        "INSERT INTO movies (title, slug, year, imdbRating, rtScore, posterUrl, featured, trending) VALUES (?,?,?,?,?,?,?,?)",
        [title, slug, year, imdb, rt, poster, imdb >= 8.5 ? 1 : 0, year >= 2024 ? 1 : 0]
      );
      const mid = r.insertId;
      const gid = genreMap[genre];
      if (gid) await conn.execute("INSERT IGNORE INTO movie_genres (movieId, genreId) VALUES (?,?)", [mid, gid]);
      inserted++;
    } catch {}
  }

  const [[{ total }]] = await conn.execute("SELECT COUNT(*) as total FROM movies");
  console.log(`✅ Inserted: ${inserted}, Skipped: ${skipped}`);
  console.log(`📊 Total movies in database: ${total}`);
  await conn.end();
}

main().catch(console.error);
